

public class ComparisonOperators {
    public static void main(String args[]){
        int a = 20 , b = 30 ;
        System.out.println("Equals to operator :" + (a == b));
        System.out.println("NOT Equals to operator :" + (a != b));
        System.out.println("Greater then to operator :" + (a > b));
        System.out.println("Less then to operator :" + (a < b));
        System.out.println("Less then Equals to operator :" + (a <= b));
        System.out.println("Greater then Equals to operator :" + (a >= b));
    }
}
